import http from 'k6/http';
import { check } from 'k6';
import { Rate } from 'k6/metrics';
import { textSummary } from '/lib/summary.js';

const BASE = `http://${__ENV.TARGET_HOST || 'swerver'}:${__ENV.TARGET_PORT || '8080'}`;
const vus = parseInt(__ENV.K6_VUS) || 100;
const duration = __ENV.K6_DURATION || '30s';
const errorRate = new Rate('errors');

const staticFiles = ['/static/index.html', '/static/styles.css', '/static/app.js'];

export const options = {
  vus: vus,
  duration: duration,
  thresholds: {
    errors: ['rate<0.05'],
    http_req_duration: ['p(95)<100'],
  },
  summaryTrendStats: ['avg', 'min', 'med', 'max', 'p(50)', 'p(75)', 'p(90)', 'p(95)', 'p(99)'],
};

export default function () {
  const roll = Math.random();

  if (roll < 0.15) {
    // 15% — full page load (batch all 3 static files)
    const responses = http.batch(staticFiles.map((f) => ['GET', `${BASE}${f}`]));
    responses.forEach((res) => {
      const ok = check(res, {
        'static batch 200': (r) => r.status === 200,
      });
      errorRate.add(!ok);
    });
  } else if (roll < 0.55) {
    // 40% — single random static file
    const file = staticFiles[Math.floor(Math.random() * staticFiles.length)];
    const res = http.get(`${BASE}${file}`);
    const ok = check(res, {
      'static 200': (r) => r.status === 200,
    });
    errorRate.add(!ok);
  } else {
    // 45% — API call
    const apiRoll = Math.random();
    let res;
    if (apiRoll < 0.5) {
      res = http.get(`${BASE}/api/data`);
    } else if (apiRoll < 0.85) {
      const id = Math.ceil(Math.random() * 3);
      res = http.get(`${BASE}/api/data/${id}`);
    } else {
      res = http.post(`${BASE}/api/data`, JSON.stringify({
        title: `k6-item-${Date.now()}`,
        body: 'Generated by k6 load test',
      }), { headers: { 'Content-Type': 'application/json' } });
    }
    const ok = check(res, {
      'api 2xx': (r) => r.status >= 200 && r.status < 300,
    });
    errorRate.add(!ok);
  }
}

export function handleSummary(data) {
  const summary = {
    scenario: 'static-api-mixed',
    timestamp: new Date().toISOString(),
    config: { vus: vus, duration: duration },
    metrics: {
      requests_total: data.metrics.http_reqs.values.count,
      requests_per_second: data.metrics.http_reqs.values.rate,
      error_rate: data.metrics.http_req_failed ? data.metrics.http_req_failed.values.rate : 0,
      latency_avg_ms: data.metrics.http_req_duration.values.avg,
      latency_p95_ms: data.metrics.http_req_duration.values['p(95)'],
      latency_p99_ms: data.metrics.http_req_duration.values['p(99)'],
      latency_max_ms: data.metrics.http_req_duration.values.max,
    },
  };
  return {
    stdout: textSummary(data, { indent: ' ' }),
    '/results/static-api-mixed.json': JSON.stringify(summary, null, 2),
  };
}
